    private void btnAgregarSensorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarSensorActionPerformed
        if (casita.getHabitaciones().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes crear una habitacion primero.");
            return;
        }

        int indiceHab = comboHabSensores.getSelectedIndex();
        Habitacion habDestino = casita.getHabitaciones().get(indiceHab);

        String tipo = comboTipoSensor.getSelectedItem().toString();
        String nombreS = txtNomSensor.getText().trim();
        String idS = txtIdSensor.getText().trim();

        if (nombreS.isEmpty() || idS.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Completa el nombre y el ID del sensor.");
            return;
        }

        switch (tipo) {
            case "Sensor de Movimiento": 
                habDestino.agregarSensor(new SensorMovimiento(nombreS, idS)); 
                break;
            case "Sensor de Temperatura": 
                habDestino.agregarSensor(new SensorTemperatura(nombreS, idS)); 
                break;
            case "Sensor de Humo": 
                habDestino.agregarSensor(new SensorHumo(nombreS, idS)); 
                break;
        }

        txtNomSensor.setText("");
        txtIdSensor.setText("");
        javax.swing.JOptionPane.showMessageDialog(this, "¡" + tipo + " agregado con éxito a " + habDestino.getNombre() + "!");
        registrarEnHistorial("Sensor instalado: " + nombreS + " en la habitación " + habDestino.getNombre());
    }//GEN-LAST:event_btnAgregarSensorActionPerformed

