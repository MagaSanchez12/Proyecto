    private void btnSimularEventoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimularEventoActionPerformed
        if (comboHabEventos.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "No hay habitaciones para simular eventos.");
            return;
        }
        VentanaPrincipal.instanciaGlobal.reporteTemporal = "";

        int idx = comboHabEventos.getSelectedIndex();
        Habitacion hab = casita.getHabitaciones().get(idx);
        String tipoEv = comboTipoEvento.getSelectedItem().toString();

        try {
            if (tipoEv.equals("Movimiento")) {
                    boolean tieneSensor = false; //vemos si tiene senspr de movimiento
                    for (Sensor s : hab.getSensores()) {
                        if (s instanceof SensorMovimiento) tieneSensor = true;
                    }
                    if (!tieneSensor) {
                        javax.swing.JOptionPane.showMessageDialog(this, "No hay Sensores de Movimiento en " + hab.getNombre(), "Sin Sensor", javax.swing.JOptionPane.WARNING_MESSAGE);
                        return;
                    }

                    String horaStr = javax.swing.JOptionPane.showInputDialog(this, "¿Que hora es? (0-23):");
                    if (horaStr == null) return; 

                    int hora = Integer.parseInt(horaStr.trim());
                    for (Habitacion h : casita.getHabitaciones()) {
                        h.setHora(hora);
                    }

                    int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, "¿Hay personas moviendose ahora en " + hab.getNombre() + "?", "Movimiento", javax.swing.JOptionPane.YES_NO_OPTION);
                    boolean hayMov = (respuesta == javax.swing.JOptionPane.YES_OPTION);

                    for (Sensor s : hab.getSensores()) {
                        if (s instanceof SensorMovimiento) s.setEstadoAlerta(hayMov);
                    }
                } 
                else if (tipoEv.equals("Temperatura")) {
                    boolean tieneSensor = false;
                    for (Sensor s : hab.getSensores()) {
                        if (s instanceof SensorTemperatura) tieneSensor = true;
                    }
                    if (!tieneSensor) {
                        javax.swing.JOptionPane.showMessageDialog(this, "No hay Sensores de Temperatura en " + hab.getNombre(), "Sin Sensor", javax.swing.JOptionPane.WARNING_MESSAGE);
                        return;
                    }

                    String tempStr = javax.swing.JOptionPane.showInputDialog(this, "Ingrese la temperatura actual (°C):");
                    if (tempStr == null) return; 

                    double temp = Double.parseDouble(tempStr.trim());
                    for (Sensor s : hab.getSensores()) {
                        if (s instanceof SensorTemperatura) {
                            ((SensorTemperatura) s).setTempAmbiente(temp);
                        }
                    }
                } 
                else if (tipoEv.equals("Humo")) {
                    boolean tieneSensor = false;
                    for (Sensor s : hab.getSensores()) {
                        if (s instanceof SensorHumo) tieneSensor = true;
                    }
                    if (!tieneSensor) {
                        javax.swing.JOptionPane.showMessageDialog(this, "No hay Sensores de Humo en " + hab.getNombre(), "Sin Sensor", javax.swing.JOptionPane.WARNING_MESSAGE);
                        return;
                    }

                    int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, "¿Se detecta humo en " + hab.getNombre() + "?", "Humo", javax.swing.JOptionPane.YES_NO_OPTION);
                    boolean hayHumo = (respuesta == javax.swing.JOptionPane.YES_OPTION);

                    for (Sensor s : hab.getSensores()) {
                        if (s instanceof SensorHumo) s.setEstadoAlerta(hayHumo);
                    }
                    
                }
                for (Sensor s : hab.getSensores()) {
                if (tipoEv.equals("Movimiento") && s instanceof SensorMovimiento) {
                    s.evaluar(hab);
                }
                else if (tipoEv.equals("Temperatura") && s instanceof SensorTemperatura) {
                    s.evaluar(hab);
                }
                else if (tipoEv.equals("Humo") && s instanceof SensorHumo) {
                    s.evaluar(hab);
                }
        }
                
                
                if (VentanaPrincipal.instanciaGlobal.reporteTemporal.isEmpty()) {//si esta vacio no paso nada
                    javax.swing.JOptionPane.showMessageDialog(this, 
                        "Todo tranquilo en " + hab.getNombre() + ".\nNo se requirio ninguna accion automatica.", 
                        "Sin Novedades", 
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, 
                        "Se ejecutaron las siguientes acciones:\n\n" + VentanaPrincipal.instanciaGlobal.reporteTemporal, 
                        "Reporte Detallado", 
                        javax.swing.JOptionPane.WARNING_MESSAGE);
                }


            } catch (NumberFormatException e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: Ingresa numeros validos.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        
        
    }//GEN-LAST:event_btnSimularEventoActionPerformed

    private void btnCalcularConsumoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularConsumoActionPerformed
        txtAreaConsumo.setText(""); // Limpiamos la pantalla

        if (casita.getHabitaciones().isEmpty()) {
            txtAreaConsumo.append("No hay habitaciones registradas.\n");
            return;
        }
        int opcion = comboOpcionesConsumo.getSelectedIndex();

        if (opcion == 0) {
            double consumoTotalCasa = 0.0;
            txtAreaConsumo.append("--- REPORTE GENERAL DE LA VIVIENDA ---\n\n");

            for (Habitacion h : casita.getHabitaciones()) {
                double consumoHab = 0.0;
                txtAreaConsumo.append(h.getNombre() + ":\n");

                for (Dispositivo d : h.getDispositivos()) {
                    if (d instanceof ConsumoEnergia) {
                        double gasto = ((ConsumoEnergia) d).obtenerConsumoActual();
                        consumoHab += gasto;
                        txtAreaConsumo.append( d.getNombre() + ": " + gasto + " kWh\n");
                    }
                }
                txtAreaConsumo.append("Consumo de:" + h.getNombre() + ": " + consumoHab + " kWh\n\n");
                consumoTotalCasa += consumoHab;
            }

            txtAreaConsumo.append("=========================================\n");
            txtAreaConsumo.append("CONSUMO TOTAL: " + consumoTotalCasa + " kWh\n");
            txtAreaConsumo.append("=========================================\n");
} 

        else {
            Habitacion h = casita.getHabitaciones().get(opcion - 1);
            double consumoHab = 0.0;

            txtAreaConsumo.append("--- REPORTE DE: " + h.getNombre().toUpperCase() + " ---\n\n");

            for (Dispositivo d : h.getDispositivos()) {
                if (d instanceof ConsumoEnergia) {
                    double gasto = ((ConsumoEnergia) d).obtenerConsumoActual();
                    consumoHab += gasto;
                    txtAreaConsumo.append( d.getNombre() + ": " + gasto + " kWh\n");
                }
            }

            txtAreaConsumo.append("\n=========================================\n");
            txtAreaConsumo.append("CONSUMO TOTAL DE LA HABITACION: " + consumoHab + " kWh\n");
            txtAreaConsumo.append("=========================================\n");
}
    }//GEN-LAST:event_btnCalcularConsumoActionPerformed

