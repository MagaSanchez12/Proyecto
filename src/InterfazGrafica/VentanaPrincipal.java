    private void btnAgregarDispositivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarDispositivoActionPerformed
        if (casita.getHabitaciones().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes crear una habitacion primero.");
            return;
        }

        int indiceHab = comboHabDispositivos.getSelectedIndex(); // numero de opcion
        Habitacion habDestino = casita.getHabitaciones().get(indiceHab); // buscamos esa hab en la memoria

        String tipo = comboTipoDispositivo.getSelectedItem().toString();
        String nombreD = txtNomDispositivo.getText().trim();
        String idD = txtIdDispositivo.getText().trim();

        // validamos que no haya dejado textos en blanco
        if (nombreD.isEmpty() || idD.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Completa el nombre y el ID del dispositivo.");
            return;
        }

        switch (tipo) {
            case "Luz":
            habDestino.agregarDispositivo(new Luz(nombreD, idD));
            break;
            case "Aire Acondicionado":
            habDestino.agregarDispositivo(new AireAcondicionado(nombreD, idD));
            break;
            case "Ventana":
            habDestino.agregarDispositivo(new Ventana(nombreD, idD));
            break;
            case "Alarma":
            habDestino.agregarDispositivo(new Alarma(nombreD, idD));
            break;
        }

        // 5. Limpiamos las casillas y mostramos éxito
        txtNomDispositivo.setText("");
        txtIdDispositivo.setText("");
        javax.swing.JOptionPane.showMessageDialog(this, "¡" + tipo + " agregado con éxito a " + habDestino.getNombre() + "!");
        registrarEnHistorial("Dispositivo creado: " + nombreD + " (" + tipo + ") en la habitación " + habDestino.getNombre());
    }//GEN-LAST:event_btnAgregarDispositivoActionPerformed

