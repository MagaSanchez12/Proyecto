    public void registrarEnHistorial(String mensaje) {
 
        java.time.LocalTime hora = java.time.LocalTime.now();
        String horaFormateada = String.format("%02d:%02d:%02d", hora.getHour(), hora.getMinute(), hora.getSecond());
        String lineaCompleta = "[" + horaFormateada + "] - " + mensaje + "\n";

        txtAreaHistorial.append(lineaCompleta);
    
        this.reporteTemporal += lineaCompleta; //copia temporal para el jOptionPane de simulacion
        if (casita != null) {
        String historialViejo = casita.getHistorialAcumulado();
        casita.setHistorialAcumulado(historialViejo + lineaCompleta);
    }
        }
